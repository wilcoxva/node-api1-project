let users = [
	{ id: "1", name: "Virginia Wilcox", bio: "A 25yo student." },
	{ id: "2", name: "Anusha Odilzoda", bio: "A 23yo bartender." },
	{ id: "3", name: "Patrick Morgan", bio: "A 36yo gamer." },
]

function getUsers() {
	return users
}

function getUserById(id) {
	return users.find(u => u.id === id)
}

function createUser(data) {
	const payload = {
		id: String(users.length + 1),
		...data,
	}

	users.push(payload)
	return payload
}

function updateUser(id, data) {
	const index = users.findIndex(u => u.id === id)
	users[index] = {
		...users[index],
		...data,
	}
	
	return users[index]
}

function deleteUser(id) {
	users = users.filter(u => u.id != id)
}

module.exports = {
	getUsers,
	getUserById,
	createUser,
	updateUser,
	deleteUser,
}